f = 1.2
i = 2
b = False
si = "3"
sf = "3.5"
sb = ""

print("Integer conversion")
print(f, ":", int(f))
print(i, ":", int(i))
print(si, ":", int(si))

print("Float conversion")
print(f, ":", float(f))
print(i, ":", float(i))
print(sf, ":", float(sf))

print("String conversion")
print(f, ":", str(f))
print(i, ":", str(i))
print(b, ":", str(b))

print("Boolean conversion")
print(f, ":", bool(f))
print(i, ":", bool(i))
print(sb, ":", bool(sb))
